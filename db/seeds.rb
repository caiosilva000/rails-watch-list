require "json"
require 'rest-client'
require "open-uri"


response = RestClient.get "https://tmdb.lewagon.com/movie/top_rated"
repos = JSON.parse(response)

puts "Getting those amaying movies "
puts "Hold on"

Movie.destroy_all
List.destroy_all
Bookmark.destroy_all

repos["results"].each do |e|
  puts e["original_title"]
  puts e["overview"]
  puts e["poster_path"]
  puts e["vote_average"]
  Movie.create(title: e["original_title"], overview: e["overview"], poster_url: "https://image.tmdb.org/t/p/original///#{e["poster_path"]}", rating: e["vote_average"])


end


